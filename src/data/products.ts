export const products: Product[] = rawProducts.map(product => ({
  ...product,
  images: product.images?.map(image => getAssetPath(image)) ?? [],
}))

// Helper functions
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

export function getProductsByCategory(
  category: string,
  subcategory?: string,
  subsubcategory?: string
): Product[] {
  return products.filter(p => {
    if (subsubcategory) {
      return p.category === category && p.subcategory === subcategory && p.subsubcategory === subsubcategory
    }
    if (subcategory) {
      return p.category === category && p.subcategory === subcategory
    }
    return p.category === category
  })
}

export function getProductsByBrand(brandSlug: string): Product[] {
  return products.filter(p => p.brand.toLowerCase().replace(/\s+/g, '-') === brandSlug)
}

export function getProductsBySaleType(saleType?: string): Product[] {
  if (!saleType) {
    return products.filter(p => p.saleType || p.originalPrice)
  }
  return products.filter(p => p.saleType === saleType)
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.featured)
}

export function getBestSellers(): Product[] {
  return products.filter(p => p.bestSeller)
}

export function getNewArrivals(): Product[] {
  return products.filter(p => p.newArrival)
}

export function searchProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase()
  return products.filter(p => 
    p.name.toLowerCase().includes(lowerQuery) ||
    p.brand.toLowerCase().includes(lowerQuery) ||
    p.category.toLowerCase().includes(lowerQuery) ||
    p.subcategory.toLowerCase().includes(lowerQuery) ||
    p.subsubcategory.toLowerCase().includes(lowerQuery) ||
    p.goal.some(g => g.toLowerCase().includes(lowerQuery))
  )
}



